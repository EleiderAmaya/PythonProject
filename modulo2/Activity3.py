# Ejercicio 3 del modulo 2 -> if - elif - else
points = 1001
if points >= 1000:
  print("¡Has alcanzado el nivel Platino! Disfruta de beneficios exclusivos.")
elif points >= 500:
  print("¡Eres miembro Oro! Gracias por tu fidelidad.")
elif points >= 100:
  print("¡Bienvenido al nivel Plata! Empieza a ganar recompensas.")
else:
  print("¡Eres miembro Bronce! ¡Sigue comprando para ganar más puntos!")
